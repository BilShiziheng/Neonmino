function RS_data.getShape(shape, rot)
    shape = shape:upper()
    local shapeData = RS_data.shapes[shape]
    if not shapeData then
        error("Invalid shape: " .. tostring(shape))
    end
    return shapeData[rot + 1]
end

local shapeToKickIdx = {
    Z = 1, S = 2, J = 3, L = 4, T = 5, O = 6, I = 7
}
local empty = { { 0, 0 } }

function RS_data.getKicks(shape, fromRot, toRot)
    shape = shape:upper()
    local shapeID = shapeToKickIdx[shape]
    if not shapeID or not kickTable[shapeID] then return empty end
    return kickTable[shapeID][fromRot * 10 + toRot] or empty
end

